#include <stdio.h>

int main() {
    int d, d1, d2, d3, d4, d5;

    // Ввід
    printf("Введіть п'ятизначне число 32057: ");
    scanf("%d", &d);

    // Розбиваємо число на окремі цифри
    d1 = d / 10000;        
    d2 = (d / 1000) % 10;   
    d3 = (d / 100) % 10;    
    d4 = (d / 10) % 10;     
    d5 = d % 10;            

    int res = d5 * 10000 + d4 * 1000 + d3 * 100 + d2 * 10 + d1;

    // Результат
    printf("Результат: %d\n", res);

    return 0;
}